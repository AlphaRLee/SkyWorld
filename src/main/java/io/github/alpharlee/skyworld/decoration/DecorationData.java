package io.github.alpharlee.skyworld.decoration;

public class DecorationData {
	public String schematicName;
	public int x;
	public int y;
	public int z;

	public DecorationData(String schematicName, int x, int y, int z) {
		this.schematicName = schematicName;
		this.x = x;
		this.y = y;
		this.z = z;
	}
}
